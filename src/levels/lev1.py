# This reads map list and assigs each cell with a unique tile.
def map_reader(surf, rows, columns, map_data, cell_size):
    for y in range(rows):
        for x in range(columns):
            index = map_data[y * columns + x]

            # Now set a number as a tile
            if index == 1:
                pg.draw.rect(surf, 'black', (x * cell_size, y * cell_size, cell_size, cell_size))

def main_lev1():
    # Initialize
    player = Player(WID/2, HIT/2)
    fading_rect = FadingRect((0, 0, WID, HIT), BLACK, fade_by=5, delay=50)  # full-screen fade

    while True:
        for event in pg.event.get():
            if event.type == pg.QUIT:
                pg.quit()
                sys.exit()

        # Bg
        WIN.fill(WHITE)

        # Grid
        draw_grid(WIN, COLUMNS, ROWS, CELL_SIZE)

        # Map reader / tile placer
        map_reader(WIN, ROWS, COLUMNS, lev1_map, CELL_SIZE)

        # Player
        player.movement()
        player.draw(WIN)

        # Fading rectangle overlay
        fading_rect.draw(WIN)
        if fading_rect.is_done():
            print("Fade complete!")  # Or trigger next state/scene

        pg.display.update()
        CLOCK.tick(FPS)


if __name__ == "__main__":
    main_lev1()