# Load the tiles that are used in this level after put in tile_surfaces -> Maprenderer
ground1 = pg.transform.scale(pg.image.load("assets/tiles/ground1.png"), (CELL_SIZE, CELL_SIZE)).convert_alpha()

# Create tile surfaces
tile_surfaces = [None, ground1] # None = 0 ground1 = 1 etc
solid_tiles = [1]




def main_lev1():
    # Initialize
    player = Player(WID/2, HIT/2)
    fading_rect = FadingRect((0, 0, WID, HIT), BLACK, fade_by=5, delay=50)  # full-screen fade
    grid_drawer = GridDrawer(COLUMNS, ROWS, CELL_SIZE)
    level_1_renderer = MapRenderer(tile_surfaces)
    openning_scene = ScenePlayer("assets/scenes/untitled_soul_opening.gif", 0, 0, WID, HIT)
    
    # Start the scene
    openning_scene.play()

    while True:
        for event in pg.event.get():
            if event.type == pg.QUIT:
                pg.quit()
                sys.exit()

            grid_drawer.handle_event(event)

        WIN.fill(WHITE)

        # If scene not finished yet
        if not openning_scene.is_done():
            openning_scene.update(loop=False)  # don't loop → end naturally
            openning_scene.draw(WIN)
        else:
            # Scene is done → now show game stuff
            grid_drawer.draw(WIN)
            level_1_renderer.render(WIN, ROWS, COLUMNS, lev1_map, CELL_SIZE)

            # Get solid rects for collision
            solid_rects = level_1_renderer.get_solid_rects(ROWS, COLUMNS, lev1_map, CELL_SIZE, solid_tiles)

            player.apply_gravity(solid_rects)
            player.movement(solid_rects)
            player.collision(solid_rects)
            player.draw(WIN)

            # Fading overlay
            fading_rect.draw(WIN)

        pg.display.update()
        CLOCK.tick(FPS)


if __name__ == "__main__":
    main_lev1()